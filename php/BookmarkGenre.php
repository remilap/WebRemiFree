<?php

require_once(dirname(__FILE__) . '/Attribute.php');

//--------------------------------------------------------------------------
// Debut classe BookmarkGenre
//--------------------------------------------------------------------------
class BookmarkGenre extends Attribute
{

  private $_nbCategories = 0;

//--------------------------------------------------------------------------
// constructor
//--------------------------------------------------------------------------
  public function BookmarkGenre($genre)
  {
    Attribute('genre', $genre);
  }

//--------------------------------------------------------------------------
// function setGenre
//--------------------------------------------------------------------------
  public function setGenre($genre)
  {
    $this->setValue($genre);
  }

//--------------------------------------------------------------------------
// function getGenre
//--------------------------------------------------------------------------
  public function getGenre()
  {
    return $this->getValue();
  }

//--------------------------------------------------------------------------
// function getNbCategories
//--------------------------------------------------------------------------
  public function getNbCategories()
  {
    return $this->$_nbCategories;
  }

//--------------------------------------------------------------------------
// function incNbCategories
//--------------------------------------------------------------------------
  public function incNbCategories()
  {
    $this->$_nbCategories++;
  }



}
//--------------------------------------------------------------------------
// Fin classe BookmarkGenre
//--------------------------------------------------------------------------

?>
